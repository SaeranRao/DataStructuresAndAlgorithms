# 编写一个程序，需要从控制台输入3个整数a、b、c,并确定它们是否可以在一个正确的算术公式（在给定的顺序）下成立，如“a+b=c”“a=b-c”“a*b=c”。
n1 = int(input("请输入第一个整数："))
n2 = int(input("请输入第二个整数："))
n3 = int(input("请输入第三个整数："))

if n1 + n2 == n3:
    print(str(n1)+'+'+str(n2)+'='+str(n3))
if n1 == n2 - n3:
    print(str(n1)+'='+str(n2)+'-'+str(n3))
if n1 * n2 == n3:
    print(str(n1)+'*'+str(n2)+'='+str(n3))
